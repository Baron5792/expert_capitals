                            <div>With gratitude to our dedicated shareholders, heads of department and our most competent workforce, we are pleased to inform you being our dedicated client and investor and the concerned general public at large that we are opening up our new branch office, an operations office in the heart of spain on August 01, 2022.&nbsp;</div><div><br></div><div>This move has been on our mind as we are putting in work in our efforts to make digital investment banking easier and accessible to our most concerned shareholders clients and the concerned general public, particularly our european business associates and clients to help ease the stress caused by the tension from the pandemic and the current presiding geopolitical distress in that part of the world which has effected a great impact in the economy of the world. &nbsp;</div><div><br></div><div>Though our new branch tagged “Operations Branch Office” might pose as a walk-in office, it has been structured to cater to clients and business associates in resolution to their specific financial issues surrounding their investment portfolios under our management. &nbsp;</div><div><br></div><div>Strictly on appointments, the main functions of this newly opened branch due to the unlifted SarsCov restrictions around europe are most importantly prioritized but not necessarily limited to: &nbsp;</div><div><br></div><div>• Attending to top classified activities for institutions, corporations and top priority personnel that are into any form of investment banking partnerships or businesses with us particularly around the resolution of banking and finance difficulties. &nbsp;&nbsp;</div><div><br></div><div>• Attending to prospective partners – individuals and corporations who wish to partake in our partnership programmes of scheme as a mean of expanding their earning potentials for sustainable financial security. &nbsp;</div><div><br></div><div>Though these abovelisted are our priorities for now, there might be more to cater to all our investors and clients from all levels and hierarchies and priority levels as the restrictions ease. &nbsp;&nbsp;</div><div><br></div><div>In the current geopolitical atmosphere, there has never been a better time to physically interact and engage with us than now. &nbsp;</div><div><br></div><div><br></div><div>We are here for you, and thank you for trusting and believing in us.</div>
